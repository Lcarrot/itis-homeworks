package archiver.task2;

public class DoubleInterval {

  private double leftBord;
  private double rightBoard;

  public DoubleInterval(double leftBord, double rightBoard) {
    this.leftBord = leftBord;
    this.rightBoard = rightBoard;
  }

  public double getLeftBord() {
    return leftBord;
  }

  public double getRightBoard() {
    return rightBoard;
  }
}
